public class BinaryTest{
	public static void main(String[] args){

		//在Java中四种进制的表示方法

		//二进制
		int n1 = 0B1010;
		//十进制
		int n2 = 1010;
		//八进制
		int n3 = 01010;
		//十六进制
		int n4 = 0X1010;

		System.out.println("0B1010 = " + n1);
		System.out.println("1010 = " + n2);
		System.out.println("01010 = " + n3);
		System.out.println("0X1010 = " + n4);
	}
}